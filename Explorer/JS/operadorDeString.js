// Operador de String (String operator)

// comparison (comparação)
console.log( 'a' == 'b') // false

// concatenation (concatenação)
console.log( 'a' + 'p')
let alpha = 'alpha'
alpha += 'bet'
console.log( alpha + 's' + 27) // alphabet

// Retorna a união de duas Strings
/*
  Temos também os string operators, os operadores de string, 
  e nós até já vimos um desses antes, o comparison operator, 
  ou ==, para os mais íntimos.

  Agora vamos ver sobre a concatenação, 
  que é o retorno da união de duas strings.

  Seu símbolo é o de + (positivo), e seu uso é simples, algo próximo de:
*/
console.log('a' + 'a')
// nesse caso, o retorno da string seria aa.